package by.com.epam.objects;

public class Point {

	private double coordinateX;
	private double coordinateY;

	public Point(double x, double y) {
		this.coordinateX = x;
		this.coordinateY = y;
	}

	public double getCoordinateX() {
		return coordinateX;
	}
	
	public double getCoordinateY() {
		return coordinateY;
	}
}
